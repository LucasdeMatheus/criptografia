def criptografar(mensagem, chave):
    indexMensagem = []
    for index in mensagem:
        for linha in range(len(vigenere_array)):
            if index == vigenere_array[linha][0]:
                indexMensagem.append(linha)

    indexChave = [] # index de cada coluna
    for index in chave:
        for coluna in range(len(vigenere_array)):
            if index == vigenere_array[0][coluna]:
                indexChave.append(coluna)

    # Criptografia
    mensagemCriptografada = ""
    for i in range(len(mensagem)):
        mensagemCriptografada += vigenere_array[indexMensagem[i]][indexChave[i]]
    return mensagemCriptografada

